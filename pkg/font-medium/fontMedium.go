package font_medium

// Medium Font 16x16 Pixels

func RuneToBitmapMedium(c rune) [][]uint8 {
	switch c {
	case 'A':
		return letterA
	case 'B':
		return letterB
	case 'C':
		return letterC
	case 'D':
		return letterD
	case 'E':
		return letterE
	case 'F':
		return letterF
	case 'G':
		return letterG
	case 'H':
		return letterH
	case 'I':
		return letterI
	case 'J':
		return letterJ
	case 'K':
		return letterK
	case 'L':
		return letterL
	case 'M':
		return letterM
	case 'N':
		return letterN
	case 'O':
		return letterO
	case 'P':
		return letterP
	case 'Q':
		return letterQ
	case 'R':
		return letterR
	case 'S':
		return letterS
	case 'T':
		return letterT
	case 'U':
		return letterU
	case 'V':
		return letterV
	case 'W':
		return letterW
	case 'X':
		return letterX
	case 'Y':
		return letterY
	case 'Z':
		return letterZ
	case '0':
		return number0
	case '1':
		return number1
	case '2':
		return number2
	case '3':
		return number3
	case '4':
		return number4
	case '5':
		return number5
	case '6':
		return number6
	case '7':
		return number7
	case '8':
		return number8
	case '9':
		return number9
	case ' ':
		return blank
	case '%':
		return percentage
	case ':':
		return doublePoint
	case '$':
		return dollarSign
	case '-':
		return minus
	case '?':
		return questionMark
	case '(':
		return openBracket
	case ')':
		return closeBracket
	case ',':
		return highComma
	case '/':
		return slash
	case '@': // Bitcoin Symbol
		return btc
	case '+': // Swiss Franc Symbol
		return chf
	default:
		return questionMark
	}
}

